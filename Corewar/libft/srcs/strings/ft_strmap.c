#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char *str;
	char *sc;

	if (!s || !f)
		return (NULL);
	str = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!str)
		return (NULL);
	sc = str;
	while (*s)
	{
		*str = f(*s);
		s++;
		str++;
	}
	*str = '\0';
	return (sc);
}
