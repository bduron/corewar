#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	char			*sc;
	unsigned int	i;

	if (!s || !f)
		return (NULL);
	str = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!str)
		return (NULL);
	i = 0;
	sc = str;
	while (*s)
	{
		*str = f(i, *s);
		s++;
		str++;
		i++;
	}
	*str = '\0';
	return (sc);
}
